package basicJavaCondition;
/*Swapping
Description
Two variables, a and b, have been initialised here for you. You must write a code to exchange the values of a and b.
The code for printing the values of a and b has already been written here for you.
Test Case
Input-Nil
Output:
a=2
b=1*/
public class Swapping {
public static void main(String[] args) {
          int a,b;
	      a=1;
	      b=2;

	int c;
	c=a;
	a=b;
	b=c;


	//The following two lines will print values of a and b
	      System.out.println("a="+a);
	      System.out.println("b="+b);
	   }

}
